export const makeUsername = (id: number): string => {
  const adjective = _getAdjective(id);
  const noun = _getNoun(id);

  return `${adjective} ${noun}`;
};

const _getAdjective = (id: number) => {
  const total = adjectives.length;
  const remainder = (id - 1) % total;
  return adjectives[remainder];
};

const _getNoun = (id: number) => {
  const total = nouns.length;
  const remainder = (id - 1) % total;
  const localNouns = [...nouns];
  let quotient = Math.floor((id - 1) / total);
  if (quotient >= total) {
    quotient = quotient % total;
  }
  const removed = localNouns.splice(0, quotient);
  const newNouns = [...localNouns, ...removed];
  return newNouns[remainder];
};
